'''Анализ частоты слов в тексте

Задание:
Напишите функцию `calculate_word_frequency`, которая получает строку текста и возвращает словарь, где ключами являются слова, а значениями - частоты этих слов в тексте. Используйте `Counter` из модуля `collections`.

Подзадачи:
1. Преобразуйте весь текст к нижнему регистру для унификации.
2. Разбейте текст на слова, учитывая только алфавитные символы (уберите пунктуацию).
3. Используйте `Counter` для подсчёта количества каждого слова.'''

from collections import Counter
import re

def calculate_word_frequency(text):
    """
    Принимает строку текста и возвращает словарь с частотами слов в этом тексте.
    
    :param text: строка текста
    :return: словарь (Counter) где ключи - слова, а значения - частоты слов
    """
    # Приводим текст к нижнему регистру для унификации
    text = text.lower()
    
    # Используем регулярное выражение для удаления неалфавитных символов и разбивки текста на слова
    words = re.findall(r'\b[a-z]+\b', text)
    
    # Считаем частоту каждого слова с использованием Counter
    word_count = Counter(words)
    
    return word_count


text = "Hello world! Hello."
result = calculate_word_frequency(text)
print(result)

