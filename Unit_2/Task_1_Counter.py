'''Подсчёт вхождений элементов в списке

Задание:
Напишите функцию `count_elements`, которая получает список элементов (которые могут повторяться) и возвращает словарь с количеством каждого уникального элемента, используя `Counter` из модуля `collections`.

Подзадачи:
1. Используйте `Counter` для подсчёта вхождений каждого элемента в списке.
2. Верните полученный словарь.

'''
from collections import Counter

def count_elements(elements):
    """
    Функция принимает список элементов и возвращает словарь,
    где ключи - это уникальные элементы из списка, а значения - количество их вхождений.
    
    :param elements: список элементов, которые могут повторяться
    :return: словарь с количеством каждого уникального элемента
    """
    # Используем Counter для подсчета элементов
    counter = Counter(elements)
    
    # Counter сам по себе является словарем, поэтому можем его напрямую вернуть
    return counter

elements = [1, 2, 2, 3, 3, 3, 4, 4, 4, 4]
result = count_elements(elements)
print(result)

